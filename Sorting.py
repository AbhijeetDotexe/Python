# Bubble Sort
def bubble_sort(arr):
    n = len(arr)
    for i in range(n - 1):
        for j in range(n - i - 1):
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]

# Selection Sort
def selection_sort(arr):
    n = len(arr)
    for i in range(n - 1):
        min_index = i
        for j in range(i + 1, n):
            if arr[j] < arr[min_index]:
                min_index = j
        arr[i], arr[min_index] = arr[min_index], arr[i]

# Insertion Sort
def insertion_sort(arr):
    n = len(arr)
    for i in range(1, n):
        key = arr[i]
        j = i - 1
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        arr[j + 1] = key

# Merge Sort
def merge_sort(arr):
    if len(arr) > 1:
        mid = len(arr) // 2
        left_half = arr[:mid]
        right_half = arr[mid:]

        merge_sort(left_half)
        merge_sort(right_half)

        i = j = k = 0
        while i < len(left_half) and j < len(right_half):
            if left_half[i] < right_half[j]:
                arr[k] = left_half[i]
                i += 1
            else:
                arr[k] = right_half[j]
                j += 1
            k += 1

        while i < len(left_half):
            arr[k] = left_half[i]
            i += 1
            k += 1

        while j < len(right_half):
            arr[k] = right_half[j]
            j += 1
            k += 1

# Example usage
my_list = [8, 2, 5, 1, 9, 3, 7, 4, 6]
print("Original List:", my_list)

# Bubble Sort
bubble_sorted_list = my_list.copy()
bubble_sort(bubble_sorted_list)
print("Bubble Sort:", bubble_sorted_list)

# Selection Sort
selection_sorted_list = my_list.copy()
selection_sort(selection_sorted_list)
print("Selection Sort:", selection_sorted_list)

# Insertion Sort
insertion_sorted_list = my_list.copy()
insertion_sort(insertion_sorted_list)
print("Insertion Sort:", insertion_sorted_list)

# Merge Sort
merge_sorted_list = my_list.copy()
merge_sort(merge_sorted_list)
print("Merge Sort:", merge_sorted_list)
